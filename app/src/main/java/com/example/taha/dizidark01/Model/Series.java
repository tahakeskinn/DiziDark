package com.example.taha.dizidark01.Model;

public class Series {
    String yapimYeri;
    String yapimYili;
    String turu;
    String konusu;
    String adminPuani;

    public String getYapimYeri() {
        return yapimYeri;
    }

    public void setYapimYeri(String yapimYeri) {
        this.yapimYeri = yapimYeri;
    }

    public String getYapimYili() {
        return yapimYili;
    }

    public void setYapimYili(String yapimYili) {
        this.yapimYili = yapimYili;
    }

    public String getTuru() {
        return turu;
    }

    public void setTuru(String turu) {
        this.turu = turu;
    }

    public String getKonusu() {
        return konusu;
    }

    public void setKonusu(String konusu) {
        this.konusu = konusu;
    }

    public String getAdminPuani() {
        return adminPuani;
    }

    public void setAdminPuani(String adminPuani) {
        this.adminPuani = adminPuani;
    }

}
